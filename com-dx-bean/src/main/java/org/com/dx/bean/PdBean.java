package org.com.dx.bean;

public class PdBean {
	
	private String distruId;
	
	private String distruName;
	
	private String sourCount;
	
	private String doCount;
	
	private String tim;

	public String getDistruId() {
		return distruId;
	}

	public void setDistruId(String distruId) {
		this.distruId = distruId;
	}

	public String getDistruName() {
		return distruName;
	}

	public void setDistruName(String distruName) {
		this.distruName = distruName;
	}

	public String getSourCount() {
		return sourCount;
	}

	public void setSourCount(String sourCount) {
		this.sourCount = sourCount;
	}

	public String getDoCount() {
		return doCount;
	}

	public void setDoCount(String doCount) {
		this.doCount = doCount;
	}

	public String getTim() {
		return tim;
	}

	public void setTim(String tim) {
		this.tim = tim;
	}
	
	

}
